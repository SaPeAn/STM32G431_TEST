  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_SPI2_Init();
  MX_TIM17_Init();
  /* USER CODE BEGIN 2 */


  uint16_t dutyCycle = 65535;
  __HAL_TIM_SET_COMPARE(&htim17, TIM_CHANNEL_1, dutyCycle);
  HAL_TIM_PWM_Start(&htim17, TIM_CHANNEL_1);
  ST7735_Init();
  ST7735_FillScreen(0);
  int16_t x = 0;
  int16_t y = 0;
  uint8_t xdir = 1;
  uint8_t ydir = 1;
  uint8_t trigger = 0;
  uint32_t temptime = timestamp;

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	if(x > 111) xdir = 0;
	if(y > 117) ydir = 0;
	if(x < 1) xdir = 1;
	if(y < 1) ydir = 1;
	if((timestamp - temptime) >= 3000) {
			temptime = timestamp;
			if(trigger) trigger = 0;
			else trigger = 1;
		}

	if(trigger) printimage(0, 0, 159, 127, gImage_butterfly, 40960);
	else printimage(0, 0, 159, 127, gImage_tiger, 40960);
	//ST7735_FillRectangle(x, y, 48, 1, 0);
	//ST7735_FillRectangle(x, y, 1, 10, 0);
	if(xdir) x++; else x--;
	if(ydir) y++; else y--;
	printtime(x, y, ST7735_LIGHTGREEN);
	HAL_Delay(20);

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
